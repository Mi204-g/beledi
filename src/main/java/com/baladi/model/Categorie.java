package com.baladi.model;

/**
 * Catégories de problèmes civiques signalables.
 */
public enum Categorie {
    VOIRIE,       // Routes abîmées, trottoirs, nids-de-poule
    ELECTRICITE,  // Coupures de courant, lampadaires défaillants
    DECHETS,      // Accumulation de déchets, poubelles non collectées
    EAU,          // Fuites, coupures d'eau
    AUTRE         // Tout problème qui ne rentre pas dans les catégories ci-dessus
}
